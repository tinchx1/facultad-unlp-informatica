package tp3.ej9;

/**
 *
 * @author tinch
 */
public class SumAndDif {
    private int sum;
    private int dif;
    
    public SumAndDif(int sum, int dif) {
        this.dif = dif;
        this.sum = sum;
    }

    public int getSum() {
        return sum;
    }

    public void setSum(int sum) {
        this.sum = sum;
    }

    public int getDif() {
        return dif;
    }

    public void setDif(int dif) {
        this.dif = dif;
    }
    
}
